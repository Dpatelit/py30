# index always start with 0 
l1 =[132564,16.262,'the','easy',"learning"]
l2 =['we','are','learning','python']
print(l1)
# in slice start index is include but end index is exclude  l1[start=0:end=size:step=1]
print("l1[4]",l1[4])
print("l1[:3]",l1[:3])
print("l1[2:4]",l1[2:4])
print("l1[2:]",l1[2:])
print("l1[::2]",l1[::2])
print("l1[::2]",l1[1::2])
print('l1+l2',l1+l2)
print("l1*2",l1*2)
